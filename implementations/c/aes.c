// 轮常量
static const uint8_t Rcon[11] = {
    0x8d, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36
};

// AES密钥扩展
static void KeyExpansion(AES_ctx* ctx, const uint8_t* key) {
    int i, j;
    uint8_t temp[4], k;
    
    // 复制初始密钥
    for (i = 0; i < ctx->key_size; i++) {
        ctx->round_key[i] = key[i];
    }
    
    // 扩展密钥
    i = ctx->key_size;
    while (i < 4 * (ctx->rounds + 1) * 4) {
        for (j = 0; j < 4; j++) {
            temp[j] = ctx->round_key[i - 4 + j];
        }
        
        if (i % ctx->key_size == 0) {
            // 循环左移
            k = temp[0];
            temp[0] = temp[1];
            temp[1] = temp[2];
            temp[2] = temp[3];
            temp[3] = k;
            
            // S-box替换
            temp[0] = sbox[temp[0]];
            temp[1] = sbox[temp[1]];
            temp[2] = sbox[temp[2]];
            temp[3] = sbox[temp[3]];
            
            // 轮常量异或
            temp[0] ^= Rcon[i / ctx->key_size];
        } else if (ctx->key_size > 24 && i % ctx->key_size == 16) {
            // 仅对AES-256
            temp[0] = sbox[temp[0]];
            temp[1] = sbox[temp[1]];
            temp[2] = sbox[temp[2]];
            temp[3] = sbox[temp[3]];
        }
        
        // 与前一轮密钥异或
        for (j = 0; j < 4; j++) {
            ctx->round_key[i] = ctx->round_key[i - ctx->key_size] ^ temp[j];
            i++;
        }
    }
}

// 初始化AES上下文
void AES_init_ctx(AES_ctx* ctx, const uint8_t* key, int key_size) {
    ctx->key_size = key_size;
    
    // 根据密钥长度设置轮数
    if (key_size == AES_128_KEY_SIZE) {
        ctx->rounds = 10;
    } else if (key_size == AES_192_KEY_SIZE) {
        ctx->rounds = 12;
    } else { // AES_256_KEY_SIZE
        ctx->rounds = 14;
    }
    
    // 扩展密钥
    KeyExpansion(ctx, key);
}

// 简化版AES加密（仅实现基本功能，非完整实现）
void AES_encrypt(const AES_ctx* ctx, uint8_t* buffer) {
    // 这里是简化的AES加密实现
    // 实际实现需要包括SubBytes, ShiftRows, MixColumns, AddRoundKey等步骤
    
    // 为简化示例，这里只进行一些基本操作
    for (int i = 0; i < AES_BLOCK_SIZE; i++) {
        buffer[i] ^= ctx->round_key[i]; // 初始轮密钥加
    }
    
    // 模拟多轮加密
    for (int round = 1; round <= ctx->rounds; round++) {
        // 模拟SubBytes
        for (int i = 0; i < AES_BLOCK_SIZE; i++) {
            buffer[i] = sbox[buffer[i]];
        }
        
        // 模拟轮密钥加
        for (int i = 0; i < AES_BLOCK_SIZE; i++) {
            buffer[i] ^= ctx->round_key[round * AES_BLOCK_SIZE + i];
        }
    }
}

// 简化版AES解密
void AES_decrypt(const AES_ctx* ctx, uint8_t* buffer) {
    // 这里是简化的AES解密实现
    
    // 模拟多轮解密（逆序）
    for (int round = ctx->rounds; round >= 1; round--) {
        // 模拟轮密钥加
        for (int i = 0; i < AES_BLOCK_SIZE; i++) {
            buffer[i] ^= ctx->round_key[round * AES_BLOCK_SIZE + i];
        }
        
        // 模拟逆SubBytes
        for (int i = 0; i < AES_BLOCK_SIZE; i++) {
            buffer[i] = rsbox[buffer[i]];
        }
    }
    
    // 初始轮密钥加
    for (int i = 0; i < AES_BLOCK_SIZE; i++) {
        buffer[i] ^= ctx->round_key[i];
    }
}

// CBC模式加密
void AES_CBC_encrypt(const AES_ctx* ctx, uint8_t* iv, uint8_t* data, size_t length) {
    uint8_t* pos = data;
    uint8_t temp[AES_BLOCK_SIZE];
    size_t i, j;
    
    // 确保数据长度是块大小的倍数
    if (length % AES_BLOCK_SIZE != 0) {
        return; // 错误处理
    }
    
    // 复制IV
    memcpy(temp, iv, AES_BLOCK_SIZE);
    
    // 对每个块进行加密
    for (i = 0; i < length; i += AES_BLOCK_SIZE) {
        // 与前一个密文块（或IV）异或
        for (j = 0; j < AES_BLOCK_SIZE; j++) {
            pos[j] ^= temp[j];
        }
        
        // 加密当前块
        AES_encrypt(ctx, pos);
        
        // 保存当前密文块用于下一轮
        memcpy(temp, pos, AES_BLOCK_SIZE);
        
        // 移动到下一个块
        pos += AES_BLOCK_SIZE;
    }
}

// CBC模式解密
void AES_CBC_decrypt(const AES_ctx* ctx, uint8_t* iv, uint8_t* data, size_t length) {
    uint8_t* pos = data;
    uint8_t temp[AES_BLOCK_SIZE];
    uint8_t temp2[AES_BLOCK_SIZE];
    size_t i, j;
    
    // 确保数据长度是块大小的倍数
    if (length % AES_BLOCK_SIZE != 0) {
        return; // 错误处理
    }
    
    // 复制IV
    memcpy(temp, iv, AES_BLOCK_SIZE);
    
    // 对每个块进行解密
    for (i = 0; i < length; i += AES_BLOCK_SIZE) {
        // 保存当前密文块
        memcpy(temp2, pos, AES_BLOCK_SIZE);
        
        // 解密当前块
        AES_decrypt(ctx, pos);
        
        // 与前一个密文块（或IV）异或
        for (j = 0; j < AES_BLOCK_SIZE; j++) {
            pos[j] ^= temp[j];
        }
        
        // 保存当前密文块用于下一轮
        memcpy(temp, temp2, AES_BLOCK_SIZE);
        
        // 移动到下一个块
        pos += AES_BLOCK_SIZE;
    }
}
